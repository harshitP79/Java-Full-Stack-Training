class FloatingPointTypeVariable{
	public static void main(String[] args) {
		float floatingPointTypeVariable;
		floatingPointTypeVariable = 1.2f;//mandatory f small or uppercase
		System.out.println(floatingPointTypeVariable);

		double doubleTypeVariable;
		doubleTypeVariable = 12.34;//you can use d or you cannot it will work
		System.out.println(doubleTypeVariable);
	}
}