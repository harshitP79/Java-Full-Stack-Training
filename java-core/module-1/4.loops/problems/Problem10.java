
public class Problem10 {

    public static void main(String[] args) {
        /* WAJP to print sum of all the numbers from the range 1 to 10 */
        int sum = 0;
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }
        System.out.println("The sum of the number in the  range of 1 to 10 is : " + sum);

    }
}
