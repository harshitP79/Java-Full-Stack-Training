
public class Problem11 {
    public static void main(String[] args) {
        /* WAJP to print product of all the numbers from the range 1 to 10 */
        int product = 1;
        for (int i = 1; i <= 10; i++) {
            product *= i;
        }
        System.out.println("The sum of the number in the  range of 1 to 10 is : " + product);

    }
}
