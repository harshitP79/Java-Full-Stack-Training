    /*
        Set Collection in Java:
        A Set is a collection that cannot contain duplicate elements. It models the mathematical set abstraction.
        1. HASHSET
        It is an implementation of the Set interface that uses a hash table for storage.
        It is present inside the java.util package.
        It was introduced in JDK 1.2.
        It does not follow insertion order.
        It does not allow duplicate elements.
        It allows only one null element.
        It is Interanally implemented using HashTable.(Hashing machanism)
        It provides fast performacr for add, remove, contains and search operations.
        It is used when uniquness is required and order is not important.

        2. LINKEDHASHSET
        It is an implementation of the Set interface that uses a hash table and a linked list for storage.
        It is present inside the java.util package.
        It was introduced in JDK 1.4.
        It follows insertion order.
        It does not allow duplicate elements.
        It allows only one null element.
        It is Internally implemented using HashTable and doubly LinkedList.
        It provides slightly slower performance than HashSet for add, remove, contains and search operations due to the additional overhead of maintaining the linked list.
        It is used when uniqueness + insertion order is required.

        3. TREESET
        It is an implementation of the Set interface that uses a red-black tree for storage.
        It is present inside the java.util package.
        It was introduced in JDK 1.2.
        
    */
